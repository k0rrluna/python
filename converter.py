def kbToB(kb):
    b = 1
    b = kb * 1024
    return b

def mToKm(m):
    km = 1
    km = m * 0.001
    return km

def gToKg(g):
    kg = 1
    kg = g * 0.001
    return kg

def sToMs(s):
    ms = 1
    ms = s *  1000
    return ms

def cToF(c):
    f = 1
    f = (c*9/5) + 32
    return f

def hzToKhz(hz):
    khz = 1
    khz = hz * 0.001
    return khz

def wToKw(w):
    kw = 1
    kw = w * 0.001
    return kw